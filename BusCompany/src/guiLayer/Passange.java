    /**
     * checks how many JSpinners have value over 0 and then adds their category
     * in the collection
     *
     * @return the list of the selected categories by the customer
     * @throws SQLException if cannot find the category in the system
     */
    private List<Category> getCategoriesFromJSpinner() throws SQLException {
        List<Category> categories = new ArrayList<>();
        for (Component comp : ticketPanel.getComponents()) {
            if (comp instanceof JSpinner) {
                if (comp.getName() != null) {
                    int index = (int) ((JSpinner) comp).getValue();
                    if (index != 0) {
                        for (int i = 0; i < index; i++) {
                            categories.add(catCtrl.getCategoryByName(comp.getName()));
                        }
                    }
                }
            }
        }
        return categories;
    }

    /**
     * for each jspinner gets its value and calculate the amount
     *
     * @return
     */
    private int getNumberOfTickets() {
        int ticketNumber = 0;
        for (Component comp : ticketPanel.getComponents()) {
            if (comp instanceof JSpinner) {
                ticketNumber += (int) ((JSpinner) comp).getValue();
            }
        }
        return ticketNumber;
    }

