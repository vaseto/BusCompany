    /**
     * fill the buttons which represent the seats in the bus Load all seats of
     * the bus as well as the all available seats then checks if the seats
     * contains the object in the available seats if so sets the seat as not
     * taken
     *
     * @param bus
     */
    protected void fillSeatPanel(Bus bus){
            try {
                TimeCtrl timeCtrl = new TimeCtrl();
                seats = bus.getSeats();
                availableSeats = seatCtrl.getAllAvailableSeats(bus.getId(), route.getId(), date, timeCtrl.getTimeById(timeId));
                for (Component comp : seatPanel1.getComponents()) {
                    if (comp instanceof JToggleButton) {
                        if (index > 0) {
                            if (availableSeats.contains(seats.get(index - 1))) {
                                ((JToggleButton) comp).setIcon(imageseat);
                                addActionListner((JToggleButton) comp);
                            } else {
                                ((JToggleButton) comp).setIcon(imageseatTaken);
                            }
                            comp.setName(index + "");
                            ((JToggleButton) comp).setToolTipText("Number: " + index);
                            index--;
                        } else {
                            comp.setVisible(false);
                        }
                    }
                }
            } catch (SQLException ex) {
                Logger.getLogger(Passange.class.getName()).log(Level.SEVERE, null, ex);
            }
    }

    /**
     * adds Action listener to the button
     *
     * @param button
     */
    private void addActionListner(JToggleButton button) {
        button.addActionListener((ActionEvent e) -> {
            if (button.isSelected()) {
                button.setIcon(imageseatTaken);
                selectedSeats.add(seatCtrl.getBusSeatByNumber(bus, Integer.parseInt(button.getName())));
            } else {
                button.setIcon(imageseat);
                selectedSeats.remove(seatCtrl.getBusSeatByNumber(bus, Integer.parseInt(button.getName())));
            }
        });
    }

