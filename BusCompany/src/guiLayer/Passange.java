    /**
     * set the prices of the tickets by deducting their price according to their
     * category
     */
    private void setPrices() {
        try {
            studentPrice = adultPrice - (adultPrice / catCtrl.getCategoryById(3).getDiscount());
            studentPrice = (double)Math.round(studentPrice * 10d) / 10d;

            childPrice = adultPrice - (adultPrice / catCtrl.getCategoryById(4).getDiscount());
            childPrice = (double)Math.round(childPrice * 10d) / 10d;

            seniorPrice = adultPrice - (adultPrice / catCtrl.getCategoryById(5).getDiscount());
            seniorPrice = (double)Math.round(seniorPrice * 10d) / 10d;
        } catch (SQLException ex) {

        }
    }

