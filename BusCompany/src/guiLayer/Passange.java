    /**
     * calculate the price of all tickets selected
     */
    private void calculatePrice() {
        double price = 0;
        int adultQuantity = (int) adultSpinner.getValue();
        int studentQuantity = (int) studentSpinner.getValue();
        int childQuantity = (int) childSpinner.getValue();
        int seniorQuantity = (int) senniorSpinner.getValue();

        if (adultQuantity != 0) {
            price += adultQuantity * adultPrice;
        }

        if (studentQuantity != 0) {
            price += studentQuantity * studentPrice;
        }

        if (childQuantity != 0) {
            price += childQuantity * childPrice;
        }
        if (seniorQuantity != 0) {
            price += seniorQuantity * seniorPrice;
        }

        totalPriceField.setText(price + "");
    }
