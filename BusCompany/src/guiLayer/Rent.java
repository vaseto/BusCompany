    /**
     * Fills the table with all buses from the collection
     *
     * @param buses to be used to fill the table
     */
    private void fillTable(List<Bus> buses) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        int row = 0;
        for (Bus bus : buses) {
            model.insertRow(row, new Object[]{bus.getId(), bus.getNoOfSeats(), bus.getPricePerDay()});
            row++;
        }
    }

