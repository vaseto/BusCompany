    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            DateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");

            fillTable(busCtrl.getAllBussesWhichAreFreeToRent(Integer.parseInt((String) jComboBox1.getSelectedItem()), formatter.parse(formatter.format((Date) fromDate.getValue())), formatter.parse(formatter.format((Date) toDate.getValue()))));

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "No connection with the server");
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Wrong format of the dates");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    /**
     *
     * @param selectedRow the row from the table
     * @return the bus with that id from the table
     * @throws SQLException
     */
    private Bus chooseBus(int selectedRow) throws SQLException {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int busId = (int) model.getValueAt(selectedRow, 0);
        return busCtrl.getBus(busId);
    }
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Date from = (Date) fromDate.getValue();
        Date to = (Date) toDate.getValue();

        LogIn in = LogIn.getInstance();
        try {
            Bus bus = chooseBus(jTable1.getSelectedRow());
            double totPrice = busCtrl.getBusPriceForTimePeriod(bus, from, to);
            if (in.getP() != null) {
                Pay p = new Pay(jPanel1, totPrice, from, to, bus);
                in.ClearAndAddPanel(p.getjPanel1());
            } else {
                in.resetMainPage();
                in.setRent(this);
                JOptionPane.showMessageDialog(null, "You will have to login in order to continue");
                in.setVisible(true);
            }
        } catch (ArrayIndexOutOfBoundsException ae) {
            JOptionPane.showMessageDialog(null, "You haven't selected a bus");
        } catch (NullPointerException ne) {
            System.out.println(ne.getMessage());
            ne.printStackTrace();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Could not save reservations");
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

